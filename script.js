(function fairyDustCursor() {
  
  var possibleColors = ["#fda917", "#ae00ff", "#044ec4"];
  var width = window.innerWidth;
  var height = window.innerHeight;
  var particles = [];
  
  function init() {
    bindEvents();
    loop();
  }
  
  function bindEvents() {
    document.addEventListener('mousemove', onMouseMove);
    document.addEventListener('touchmove', onTouchMove);
    document.addEventListener('touchstart', onTouchMove);
    window.addEventListener('resize', onWindowResize);
  }
  
  function onWindowResize(e) {
    width = window.innerWidth;
    height = window.innerHeight;
  }
  
  function onTouchMove(e) {
    if(e.touches.length > 0) {
      for(var i = 0; i < e.touches.length; i++) {
        addParticle(e.touches[i].clientX, e.touches[i].clientY, possibleColors[Math.floor(Math.random() * possibleColors.length)]);
      }
    }
  }
  
  function onMouseMove(e) {    
    addParticle(e.clientX, e.clientY, possibleColors[Math.floor(Math.random() * possibleColors.length)]);
  }

  function addParticle(x, y, color) {
    var particle = new Particle();
    particle.init(x, y, color);
    particles.push(particle);
  }
  
  function updateParticles() {
    for(var i = 0; i < particles.length; i++) {
      particles[i].update();
    }
    for(var i = particles.length - 1; i >= 0; i--) {
      if(particles[i].lifeSpan < 0) {
        particles[i].die();
        particles.splice(i, 1);
      }
    }
  }
  
  function loop() {
    requestAnimationFrame(loop);
    updateParticles();
  }
  
  function Particle() {
    this.character = "☆";
    this.lifeSpan = 120;
    this.initialStyles = {
      "position": "absolute",
      "display": "block",
      "pointerEvents": "none",
      "z-index": "10000000",
      "fontSize": "11px",
      "fontWeight": "bold",
      "lineHeight": "1",
      "textAlign": "center"
    };

    this.init = function(x, y, color) {
      this.velocity = {
        x: (Math.random() - 0.5) * 0.8,
        y: (Math.random() - 0.5) * 0.8 - 0.5
      };
      
      this.position = {x: x, y: y};
      this.initialStyles.color = color;
      
      this.element = document.createElement('span');
      this.element.innerHTML = this.character;
      applyProperties(this.element, this.initialStyles);
      this.update();
      
      document.body.appendChild(this.element);
    };
    
    this.update = function() {
      this.position.x += this.velocity.x;
      this.position.y += this.velocity.y;
      this.lifeSpan--;
      
      var scaleValue = 0.3 + (this.lifeSpan / 120) * 0.7;
      this.element.style.left = this.position.x + "px";
      this.element.style.top = this.position.y + "px";
      this.element.style.transform = "translate(-50%, -50%) scale(" + scaleValue + ")";
    }
    
    this.die = function() {
      if(this.element && this.element.parentNode) {
        this.element.parentNode.removeChild(this.element);
      }
    }
  }
  
  function applyProperties(target, properties) {
    for(var key in properties) {
      target.style[key] = properties[key];
    }
  }
  
  init();
})();




